    let a_id = app.ACTIVITYID
    let list=[]
    for (var i in a_id){
      const db = wx.cloud.database({
        env: 'testdemo-cba87d'
      })
      db.collection('ActivityApply').doc(a_id[i])
      .get({
        success: res => {
          list.unshift(res.data)
          this.setData({
            Activity_list: list
          })
        }
      })
    }
    
